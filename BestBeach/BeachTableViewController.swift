    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "reuseIdentifier", for: indexPath) as! BeachTableViewCell

        let beaches = data.places[indexPath.row]
        let image = UIImage(named: beaches.filename)
        
        cell.bkImageView.image = image
        cell.beachNameLabel.text = beaches.heading
        
        return cell
    }
    

